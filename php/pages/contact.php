<p>Générateur de mentions légales (gratuit)

Nous mettons à votre disposition un utilitaire permettant de générer gratuitement vos mentions légales pour votre site internet. Le générateur de mentions légales permet l’exportation de vos informations légales au format HTML qu’il faudra tout simplement copier-coller sur votre site web. Vous avez la possibilité de générer vos mentions légales, pour un particulier ou une entreprise.<br />
GÉNÉRER MES MENTIONS LÉGALES<br />
Pourquoi faire ?<br />
En France, conformément à la loi pour la confiance dans l’économie numérique, si un organisme met en place un « un service de communication au public » en ligne, il doit fournir un certain nombre de mentions légales dans un format ouvert (généralement au format HTML). Ces dispositions ne sont dans la pratique pas respectées par nombre de petits sites, mais les sites ne les respectant pas encourent 75 000 euros d’amende.<br />
Obligations de l’éditeur<br />
Pour un site vitrine ou un site marchand, où les informations des différents internautes sont recueillies, il est nécessaire de déclarer sa base de donnés clients à la CNIL (Commission Nationale de l’Information et des Libertés). La non déclaration peut entrainer une peine de cinq années d’emprisonnement et 300.000 euros d’amende. Cette déclaration peut se faire sur le site de la CNIL.<br />
L’éditeur doit indiquer aux internautes qu’ils ont accès aux informations les concernant et peuvent les modifier ou en demander la suppression.
Contenu des Mentions Légales<br />
Les mentions légales doivent contenir un certain nombres d’informations (éditeur, directeur de publication, hébergeur, développeur, etc…) conformes à l’article 6 de la loi n° 2004-575 du 21 juin 2004 pour la confiance dans l’économie numérique :<br />
